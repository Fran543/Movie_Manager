    private void init() {
        try {
            initValidation();
            initRepository();
            initTable();
            initDragNDrop();
            
            allMoviesModel = new DefaultListModel();
            moviesModel = new DefaultListModel();

            loadMoviesModel();
            
            lsMovies.setModel(moviesModel);

            btnRemoveMovie.setEnabled(false);
            clearForm();


        } catch (Exception ex) {
            Logger.getLogger(Directors.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);

        }
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfFirstName, tfLastName);
        errorLabels = Arrays.asList(lbFirstNameError, lbLastNameError);
    }

    private void initRepository() throws Exception {
        repository = RepositoryFactory.GetMovieRepository();
        directorRepository = RepositoryFactory.GetDirectorRepository(   );
    }

    private void initTable() throws Exception {
        tbDirectors.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbDirectors.setAutoCreateRowSorter(true);
        tbDirectors.setRowHeight(25);
        directorsTableModel = new PersonTableModel(new ArrayList<>(directorRepository.SelectDirectors()));
        tbDirectors.setModel(directorsTableModel);
    }

    private void initDragNDrop() {
        lsAllMovies.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        lsAllMovies.setDragEnabled(true);
        lsAllMovies.setTransferHandler(new Directors.ExportMovieTransferHandler());
      
        lsMovies.setDropMode(DropMode.ON);
        lsMovies.setTransferHandler(new Directors.ImportMovieTransferHandler());
    }

    private void showDirector() {
        clearForm();
        int selectedRow = tbDirectors.getSelectedRow();		
        int rowIndex = tbDirectors.convertRowIndexToModel(selectedRow);
        int selectedDirectorId = (int) directorsTableModel.getValueAt(rowIndex, 0);

        try {
            Optional<Director> optDirector = directorRepository.SelectDirector(selectedDirectorId);
            if (optDirector.isPresent()) {
                selectedDirector = optDirector.get();
                fillForm(selectedDirector);
            }
        } catch (Exception ex) {
            Logger.getLogger(MovieManager.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to show movie!");
        }
    }

    private void clearForm() {
       
        validationFields.forEach(e -> e.setText(""));
        errorLabels.forEach(e -> e.setText(""));
        
        lbIcon.setIcon(null);
        selectedDirector = null;
        
        moviesSet.clear();
        
        moviesModel.clear();

    }

    private void fillForm(Director director) throws Exception {
        
        loadMoviesModel(director.getId());
        
        tfFirstName.setText(director.getFirstName());
        tfLastName.setText(director.getLastName());
        setIcon(lbIcon, new File(directorRepository.SelectDirectedMovies(director.getId()).get(0).getImagePath()));
    }

    private void loadMoviesModel(int id) throws Exception {
        List<Movie> movies = directorRepository.SelectDirectedMovies(id);
        movies.forEach(movie -> moviesSet.add(movie));
        moviesModel.clear();
        moviesSet.forEach(movie -> moviesModel.addElement(movie));
    }

    private void loadMoviesModel() throws Exception {
        List<Movie> movies = repository.SelectMovies();
        allMoviesModel.clear();
        movies.forEach(movie -> allMoviesModel.addElement(movie));
        lsAllMovies.setModel(allMoviesModel);

    }

    private void setIcon(JLabel label, File file) {
        try {
            label.setIcon(IconUtils.createIcon(file.getAbsolutePath(), label.getWidth(), label.getHeight()));
        } catch (IOException ex) {
            Logger.getLogger(MovieManager.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to set icon!");
        }
    }

    private boolean formValid() {
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setText(validationFields.get(i).getText().trim().isEmpty() ? "X" : "");

        }

        return ok;
    }
    
    private class ExportMovieTransferHandler extends TransferHandler {

        @Override
        public int getSourceActions(JComponent c) {
            return COPY;
        }

        @Override
        public Transferable createTransferable(JComponent c) {
            return new MovieTransferable(lsAllMovies.getSelectedValue());
        }
    }
    
    private class ImportMovieTransferHandler extends TransferHandler {

        @Override
        public boolean canImport(TransferHandler.TransferSupport support) {
            return support.isDataFlavorSupported(MovieTransferable.MOVIE_FLAVOR);
        }

        @Override
        public boolean importData(TransferHandler.TransferSupport support) {
            Transferable transferable = support.getTransferable();
            try {
                Movie add = (Movie) transferable.getTransferData(MovieTransferable.MOVIE_FLAVOR);
                if(moviesSet.add(add)){
                    moviesModel.addElement(add);
                    return true;
                }
            } catch (UnsupportedFlavorException | IOException ex) {
                Logger.getLogger(Movies.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(Movies.class.getName()).log(Level.SEVERE, null, ex);
            }
            return false;
        }
    }

