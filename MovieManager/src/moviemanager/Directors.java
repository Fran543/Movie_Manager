    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if (formValid()) {
            try {
                Director director = new Director(
                        tfFirstName.getText().trim(),
                        tfLastName.getText().trim()                        
                );
                int actorId = directorRepository.CreateDirector(director);
                
                moviesSet.forEach(movie -> {
                    try {
                        repository.InsertDirectorInMovie(movie.getId(), actorId);
                    } catch (Exception ex) {
                        Logger.getLogger(Movies.class.getName()).log(Level.SEVERE, null, ex);
                        MessageUtils.showErrorMessage("Error", "Unable to insert director in movie!");

                    }
                });
      
                directorsTableModel.setPersons(new ArrayList<>(directorRepository.SelectDirectors()));

                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(MovieManager.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to create director!");
            }
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedDirector == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose director to update");
            return;
        }
        if (formValid()) {
            try {

                selectedDirector.setFirstName(tfFirstName.getText().trim());
                selectedDirector.setLastName(tfLastName.getText().trim());
                
                directorRepository.DeleteDirectedMovies(selectedDirector.getId());
                moviesSet.forEach(movie -> {
                    try {
                        repository.InsertDirectorInMovie(movie.getId(), selectedDirector.getId());
                    } catch (Exception ex) {
                        Logger.getLogger(Movies.class.getName()).log(Level.SEVERE, null, ex);
                    }
                });

                directorRepository.UpdateDirector(selectedDirector.getId(), selectedDirector);
                directorsTableModel.setPersons(new ArrayList<>(directorRepository.SelectDirectors()));
                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(Movies.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to update director!");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedDirector == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose director to delete");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete actor",
                "Do you really want to delete actor?") == JOptionPane.YES_OPTION) {
            try {
                directorRepository.DeleteDirectedMovies(selectedDirector.getId());
                directorRepository.DeleteDirector(selectedDirector.getId());
                directorsTableModel.setPersons(new ArrayList<>(directorRepository.SelectDirectors()));

                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(Movies.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete director!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


